package com.bitsofproof.supernode.connector4;

public class Connector4Exception extends Exception
{
	public Connector4Exception (String message)
	{
		super (message);
	}

	public Connector4Exception (String message, Throwable cause)
	{
		super (message, cause);
	}

	public Connector4Exception (Throwable cause)
	{
		super (cause);
	}

	public Connector4Exception (String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
	{
		super (message, cause, enableSuppression, writableStackTrace);
	}
}
